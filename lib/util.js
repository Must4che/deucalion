class Util {
    static applySettings(self, setting) {
        const hydratedSelf = self;
        Object.keys(setting).forEach((key) => {
            hydratedSelf[key] = setting[key];
        });
        return hydratedSelf;
    }

    static removeAttributes(self) {
        const purgedSelf = self;
        Object.keys(self).forEach((key) => {
            delete purgedSelf[key];
        });
        return purgedSelf;
    }
}

module.exports = Util;
