class Server {
    constructor() {
        this.defaultPort = defaultPort;
    }

    async start(port = this.defaultPort) {
        try {
            const server = await createServer({ port });
            return server;
        } catch (e) {
            process.exit(1);
        }
    }
}

module.exports = new Server();
